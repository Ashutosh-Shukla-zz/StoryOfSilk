package com.sos.common.beans;

public class ApparelBean {

	private int apparelId;

	private String apparelName;

	private String apparelDesc;

	private String baseImage;

	private Boolean isenabled;

	private String gender;

	public int getApparelId() {
		return apparelId;
	}

	public void setApparelId(int apparelId) {
		this.apparelId = apparelId;
	}

	public String getApparelName() {
		return apparelName;
	}

	public void setApparelName(String apparelName) {
		this.apparelName = apparelName;
	}

	public String getApparelDesc() {
		return apparelDesc;
	}

	public void setApparelDesc(String apparelDesc) {
		this.apparelDesc = apparelDesc;
	}

	public String getBaseImage() {
		return baseImage;
	}

	public void setBaseImage(String baseImage) {
		this.baseImage = baseImage;
	}

	public Boolean getIsenabled() {
		return isenabled;
	}

	public void setIsenabled(Boolean isenabled) {
		this.isenabled = isenabled;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

}
